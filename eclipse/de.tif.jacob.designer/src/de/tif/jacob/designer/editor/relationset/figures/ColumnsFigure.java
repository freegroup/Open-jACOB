package de.tif.jacob.designer.editor.relationset.figures;

import org.eclipse.draw2d.AbstractBorder;
import org.eclipse.draw2d.ColorConstants;
import org.eclipse.draw2d.Figure;
import org.eclipse.draw2d.FlowLayout;
import org.eclipse.draw2d.Graphics;
import org.eclipse.draw2d.IFigure;
import org.eclipse.draw2d.geometry.Insets;
import de.tif.jacob.designer.editor.Constants;

/**
 * Figure used to hold the column labels
 * @author Phil Zoio
 */
public class ColumnsFigure extends Figure
{

	public ColumnsFigure()
	{
		FlowLayout layout = new FlowLayout();
		layout.setMinorAlignment(FlowLayout.ALIGN_LEFTTOP);
		layout.setStretchMinorAxis(false);
		layout.setHorizontal(false);
		setLayoutManager(layout);
		setBackgroundColor(Constants.COLOR_TABLE);
		setOpaque(true);
	}
}