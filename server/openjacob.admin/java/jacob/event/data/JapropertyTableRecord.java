package jacob.event.data;

import jacob.model.Japroperty;
import de.tif.jacob.cluster.ClusterManager;
import de.tif.jacob.core.data.IDataTableRecord;
import de.tif.jacob.core.data.IDataTransaction;
import de.tif.jacob.core.data.event.DataTableRecordEventHandler;
import de.tif.jacob.core.data.impl.DataTransaction;

/**
 * @author Andreas Sonntag
 *
 * To change the template for this generated type comment go to
 * Window>Preferences>Java>Code Generation>Code and Comments
 */
public class JapropertyTableRecord extends DataTableRecordEventHandler
{
  static public final transient String RCS_ID = "$Id: JapropertyTableRecord.java,v 1.2 2010/01/20 02:04:26 ibissw Exp $";
  static public final transient String RCS_REV = "$Revision: 1.2 $";

  
	/* (non-Javadoc)
	 * @see de.tif.jacob.core.data.event.DataTableRecordEventHandler#afterNewAction(de.tif.jacob.core.data.IDataTableRecord, de.tif.jacob.core.data.IDataTransaction)
	 */
	public void afterNewAction(IDataTableRecord tableRecord, IDataTransaction transaction) throws Exception
	{
    // nothing to do here
  }

	/* (non-Javadoc)
	 * @see de.tif.jacob.core.data.event.DataTableRecordEventHandler#afterDeleteAction(de.tif.jacob.core.data.IDataTableRecord, de.tif.jacob.core.data.IDataTransaction)
	 */
	public void afterDeleteAction(IDataTableRecord tableRecord, IDataTransaction transaction) throws Exception
	{
    // nothing to do here
  }

	/* (non-Javadoc)
	 * @see de.tif.jacob.core.data.event.DataTableRecordEventHandler#beforeCommitAction(de.tif.jacob.core.data.IDataTableRecord, de.tif.jacob.core.data.IDataTransaction)
	 */
	public void beforeCommitAction(IDataTableRecord tableRecord, IDataTransaction transaction) throws Exception
	{
    if (tableRecord.isDeleted())
      return;
    
    tableRecord.setStringValue(transaction, Japroperty.changed, "NOW");
    tableRecord.setStringValue(transaction, Japroperty.changedby, ((DataTransaction) transaction).getUser().getLoginId());
  }

	/* (non-Javadoc)
	 * @see de.tif.jacob.core.data.event.DataTableRecordEventHandler#afterCommitAction(de.tif.jacob.core.data.IDataTableRecord)
	 */
	public void afterCommitAction(IDataTableRecord tableRecord) throws Exception
	{
    String propertyName = tableRecord.getStringValue(Japroperty.name);
    ClusterManager.getProvider().propagatePropertyChange(propertyName);
  }
}
